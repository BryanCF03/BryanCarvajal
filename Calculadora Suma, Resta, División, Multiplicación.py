#Calculadora suma, resta, división y multiplicación

numUno=float(input("Digite su primer Numero: "))
numDos=float(input("Digite su segundo Numero: "))

print("El resultado de",numUno,"+",numDos,"=",numUno+numDos)
print("El resultado de",numUno,"-",numDos,"=",numUno-numDos)
print("El resultado de",numUno,"*",numDos,"=",numUno*numDos)
print("El resultado de",numUno,"/",numDos,"=",numUno/numDos)
